const getCourtUrl = (courtText: string): string => {
  if (courtText) {
    if (courtsMap.has(courtText)) {
      return courtsMap.get(courtText)
    }
    if (courtsNameMapping.has(courtText)) {
      const courtNewText = courtsNameMapping.get(courtText)
      if (courtsMap.has(courtNewText)) {
        return courtsMap.get(courtNewText)
      }
    }
  }
  // eslint-disable-next-line no-console
  console.error(`Court not found: [${courtText}]`)
  return null
}

